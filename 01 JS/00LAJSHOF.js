// *****************************************
var strJs = "javascript is awesome"
let noToVowelsJs = (arr) => {
  return arr.split('').filter(value => value !== "a" && value !=='e' && value !=='i' && value !=='o').join('')
}
console.log(noToVowelsJs(strJs));
